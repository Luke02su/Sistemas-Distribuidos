    private void btnVerificarConexaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerificarConexaoActionPerformed
        // TODO add your handling code here:
        try {
            Socket con = new Socket(txtIP.getText(), Integer.parseInt(txtPorta.getText()));
            saida = new PrintStream(con.getOutputStream());
            
            String meuNome = txtNome.getText();
            saida.println(meuNome);
            
            Thread t = new Thread(new GerenciarConexao(this, con));
            t.start();
        } catch(IOException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnVerificarConexaoActionPerformed

